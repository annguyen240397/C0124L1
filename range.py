##In tu 1 den 10
for i in range(1,11,1):
    print(i, end = ' ')
##In tu 10 den 1
for k in range(10,0,-1):
    print(k, end = ' ')
##In so chan
for m in range(2,11,2):
    print(m, end = ' ')
##In so le
for n in range(1,10,2):
    print(n, end = ' ')
##In so chan vs if
for b in range(1,11,1):
    if b % 2 == 0 :
        print(b, end = ' ')
##In so le vs if
for v in range(1,11,1):
    if v % 2 == 1 :
        print(v, end = ' ')